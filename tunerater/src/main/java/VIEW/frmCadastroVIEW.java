    private void cadastrar() {

        try {
            String nome, email, senha;

            nome = txtNome.getText();
            email = txtEmail.getText();
            senha = txtSenha.getText();

            UsuarioDTO objusuariodto = new UsuarioDTO();
            objusuariodto.setNome_usuario(nome);
            objusuariodto.setEmail_usuario(email);
            objusuariodto.setSenha_usuario(senha);

            UsuarioDAO objusuariodao = new UsuarioDAO();

            ResultSet rsusuariodao = objusuariodao.autenticacaoUsuario(objusuariodto);

            if (rsusuariodao.next()) {
                JOptionPane.showMessageDialog(null, "Email j√° Cadastrado");

            } else {

                objusuariodao.cadastrarUsuario(objusuariodto);
                frmLoginVIEW objfrmloginview = new frmLoginVIEW();
                objfrmloginview.setVisible(true);
                dispose();

            }
        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, "FRMCadastrar" + e);
        }
    }

