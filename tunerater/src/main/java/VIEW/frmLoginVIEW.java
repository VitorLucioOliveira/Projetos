    private void Logar() {

        try {
            String email_usuario = txtEmailUsuario.getText();
            String senha_usuario = txtSenhaUsuario.getText();

            UsuarioDTO objUserDTO = new UsuarioDTO();

            objUserDTO.setEmail_usuario(email_usuario);
            objUserDTO.setSenha_usuario(senha_usuario);

            UsuarioDAO objusuariodao = new UsuarioDAO();

            ResultSet rsusuariodao = objusuariodao.autenticacaoUsuario(objUserDTO);

            if (rsusuariodao.next()) {
                //chamar tela que quero abrir/principal
                frmPrincipalVIEW objfrmprincipalview = new frmPrincipalVIEW();
                objfrmprincipalview.setVisible(true);
                dispose();

            } else {
                //deu merda
                JOptionPane.showMessageDialog(null, "Usuario ou Senhas Invalida");

            }

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, "FRMLOGIN" + e);
        }

    }

